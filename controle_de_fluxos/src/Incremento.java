public class Incremento {
    /* Método principal da classe */

    public static void main(String[] args) {
        int x = 8;
        int y = ++x;// pós-incremento

        System.out.println("X =" + x);
        System.out.println("Y =" + y);
    }// fim do método
}// fim da classe
