public class While {
    /* Método principal da classe */
    public static void main(String[] args) {
        // Fluxo de repetição usando While
        int i = 0;
        // a variável i recebe o valor zero (0)
        while (i <= 5) {
            // enquanto i for menor que 2
            System.out.println(i); // imprima valor de i
            i++;
            // incremento da variável i.
        } // fim do laço
    }// fim da método
}// fim da classe